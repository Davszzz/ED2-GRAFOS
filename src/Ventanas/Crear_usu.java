    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here
        
        int cod = uController.size()+1;
        String usuario = this.txtusu.getText();
        String pas = new String(txtpas.getPassword());
        String pas1 = new String(txtpas1.getPassword());
        int codc = this.comboEmpresa.getSelectedIndex();
        ValidarLogin();
        
        if(txtusu.getText().isEmpty() || pas.isEmpty() || !pas.equals(pas1) || pas.isEmpty() || comboEmpresa.getSelectedIndex() == 0)
        {
            String mensaje = "Debe completar los siguientes campos correctamente:\n" + camposIncorrectos.toString();
            JOptionPane.showMessageDialog(null, mensaje, "Campos incorrectos", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            Usuario usu = new Usuario();
            usu.setCod(cod);
            usu.setUsuario(usuario);
            usu.setContra(pas);
            usu.setCodc(codc);
        
            uController.add(usu);
            uController.grabar();
            
            JOptionPane.showMessageDialog(null, "Registro creado satisfactoriamente","臓Felicitaciones!", JOptionPane.WARNING_MESSAGE);
            InicioSesion inicio = new InicioSesion();
            inicio.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

